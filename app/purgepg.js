const { Sequelize } = require("sequelize");
const chalk = require("chalk");
const fs = require("fs");

if (fs.existsSync(".env")) {
  require("dotenv").config({ path: ".env" });
}

const sequelize = new Sequelize(process.env.DATABASE_URL, {
  dialectOptions: {
    ssl: {
      require: true,
      rejectUnauthorized: false,
    },
  },
  protocol: "postgres",
  dialect: "postgres",
  logging: false,
});

async function cleanDatabase() {
  try {
    await sequelize.authenticate();
    const tableNames = await sequelize.showAllSchemas();

    for (const tableName of tableNames) {
      await sequelize.query(`DROP TABLE IF EXISTS "${tableName}" CASCADE`);
      console.log(chalk.green(`Dropped table: ${tableName}`));
    }

    console.log(chalk.green("Database cleaned successfully."));
  } catch (err) {
    console.error(
      chalk.red("An error occurred while cleaning the database:"),
      err
    );
  } finally {
    await sequelize.close();
  }
}

